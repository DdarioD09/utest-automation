package co.com.choucair.challenge.utestautomation.model;

public class DevicesFormData {
    private String computer;
    private String version;
    private String pcLanguage;
    private String mobile;
    private String model;
    private String os;

    public String getComputer() {
        return computer;
    }

    public void setComputer(String computer) {
        this.computer = computer;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getPcLanguage() {
        return pcLanguage;
    }

    public void setPcLanguage(String pcLanguage) {
        this.pcLanguage = pcLanguage;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }
}
